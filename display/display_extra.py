def centre_string(string, length):
    # Add spaces to the right and left until the desired length is reached
    counter = 0

    while len(string) < length:
        if counter % 2 == 0:
            string = string + " "
        else:
            string = " " + string

        counter += 1

    return string


def create_pixel_buffer(size_x, size_y):
    # Add lists into a list to create a 2D array
    pixel_buffer = []

    for y in range(0, size_y):
        temp = []
        for x in range(0, size_x):
            temp.append(" ")
        pixel_buffer.append(temp)

    return pixel_buffer


